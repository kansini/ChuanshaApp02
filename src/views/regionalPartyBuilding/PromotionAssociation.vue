        第一章 总 则<br>
        　　第一条<br>
        本会的指导思想和宗旨：坚持以邓小平理论和“三个代表”重要思想、科学发展观为指导，深入贯彻习近平总书记系列重要讲话精神，按照全面从严治党，加强党的执政能力建设和加强党的先进性、纯洁性建设的要求，紧紧围绕推动发展、服务群众、凝聚人心、促进和谐的目标，以“契约化共建、项目化推进、社会化运行、制度化保障”的方式，统筹党内资源与社会资源，统筹镇属资源和区域资源，探索加强新时期党建工作、推进党的执政方式现代化的新思路、新途径，切实提高区域化党建工作的制度化和规范化水平，有效提升服务群众、服务发展的整体效应和工作能级，以党的建设引领推动区域经济社会建设和各单位自身发展，紧紧围绕浦东“2+2+2+1”战略布局，立足推动发展城乡一体化，为进一步加强环境综合整治，努力为实现建设美丽合庆的总目标提供可靠的政治和组织保证，谱写一流党建促一流开发的新篇章。<br>
