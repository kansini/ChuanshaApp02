        第二章 会员单位<br>
        　　第三条 本会由单位会员组成。<br>
        　　第四条 申请加入本会的单位，必须承认本会章程，自愿加入本会，自觉执行本会决议，积极参加本会活动。本会面向合庆镇区域内的驻区单位、学校院所、企事业单位、社会组织、现役部队等的党组织开放。
        　　第五条 加入本会可通过会员单位推荐、秘书处提名、单位自愿申请等方式申请入会，申请程序是：
        　　（一）向本会提交入会申请表；
        　　（二）秘书处委派专人与申请单位开展磋商；
        　　（三）经本会审议通过。
        　　第六条 本会会员单位享有以下权利：
        　　（一）参加本会的活动权；
        　　（二）获得本会服务、运用本会资源的优先权；
        　　（三）对本会工作的知情权、建议权和监督权；
        　　（四）入会自愿、退会自由权。
        　　第七条 本会会员单位履行以下义务：
        　　（一）遵守本会的章程，执行本会的决议；
        　　（二）积极参加本会各项活动，承担、执行本会委托的工作和任务；
        　　（三）向本会提供推动区域化党建工作的有关信息资料、研究成果以及相关建议。
