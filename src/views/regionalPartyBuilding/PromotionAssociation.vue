        　　（一）筹备和召开会员大会；
        　　（二）执行会员大会的决议；
        　　（三）向会员大会报告工作；
        　　（四）制定内部规章制度；
        　　（五）指导全镇区域化党建工作开展；
        　　（六）牵头推进党建活动项目化运作。
        　　第十一条 本会副会长除承担会员单位相应义务外，还应积极履行以下职责：
        　　（一）带头执行本会各项决议，为本会发展建言献策；
        　　（二）深入会员单位了解情况，积极推动本会工作落实；
        　　（三）充分发挥自身专业优势，主动设计或认领工作项目。
