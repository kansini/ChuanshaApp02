            <p>
        　　上海市浦东新区区域化党建促进会合庆镇分会，由中共上海市浦东新区合庆镇委员会和合庆镇区域内的相关驻区单位、学校院所、企事业单位、社会组织、现役部队等共同发起成立。<br>
