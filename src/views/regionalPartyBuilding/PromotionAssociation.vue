        　　第九条
        本会设会长1人，由合庆镇党委书记担任；常务副会长1人，由合庆镇党委副书记担任；执行副会长1人，由合庆镇党委组织委员担任；另设副会长若干名，由镇党委在会员单位中提名产生。分会秘书长由合庆镇党委组织委员兼任，副秘书长由合庆镇党建办主任担任。
